<p>A transportadora declara-se livre de carga e descarga, atividades extras que não são atribuições do serviço de transporte mas que são solicitadas pelos clientes, têm seu custo próprio e serão cobradas de forma complementar ao frete.</p>

<h2>5.5 Frete Dedicado/Expresso</h2>
<p>Nos casos de solicitação de entregas na qual o cliente não aceita o compartilhamento da sua carga com a de outras empresas ou nas situações em que o mesmo não pode aguardar o prazo fracionado devido a urgência imposta, o solicitante deve arcar com o frete dedicado/fechado ou expresso mesmo que a carga não ocupe a totalidade do mesmo.</p>

<h2>5.6 Serviço de Montagem de Paletes</h2>
<p>O serviço de montagem de paletes ou unitização de carga pelo transportador será cobrado à parte. Cobrança realizada por palete padrão manuseado/montado ou unidade montada.</p>

<h2>5.7 Serviço de Embalagem</h2>
<p>O serviço de embalagem também poderá ser cobrado a parte, dependendo do tamanho e quantidade de volumes.</p>

<h2>5.8 Contabilização dos Prazos de Entrega</h2>
<p>Todos os prazos de entrega devem, obrigatoriamente, ser contabilizados em dias úteis, seguindo o serviço regular de transporte, de segunda a sexta dentro do horário comercial. Todo e quaisquer ambiguidade na interpretação sobre os prazos não devem ser interpretados contra a Transportadora.</p>

