<h1>1. Dados e Informações - DigoTurismo</h1>

<h2>1.1 Informações Necessárias</h2>
<p>É obrigatório e de responsabilidade do contratante fornecer todas as informações necessárias à DigoTurismo no momento da solicitação do serviço referente à documentação fiscal ou equivalente nos termos da lei. Toda cotação é efetuada conforme dados fornecidos pelo solicitante, qualquer alteração nas informações necessita ser realizada antes do fechamento do serviço, pois o valor poderá sofrer alterações sem aviso prévio, sendo acatados dados declarados em nota fiscal. Em caso de informação inexata ou falsa descrição na relação de cargas, descrição, valor de produto e/ou encargos, será o transportador indenizado pelo prejuízo que sofrer, devendo a ação respectiva ser ajuizada no prazo de cento e vinte dias, a contar daquele ato, sob pena de decadência.</p>

<h2>1.2 Dimensionador de Carga</h2>
<p>Trabalhamos com dimensionador de carga, ou seja, toda e qualquer mercadoria obrigatoriamente passa por um processo de verificação de peso e cubagem. Havendo qualquer tipo de divergência nas informações apresentadas na cotação, haverá alteração no valor do serviço, sem aviso prévio.</p>

<h2>1.3 Alterações e Exigências</h2>
<p>Os valores informados em sua cotação não contemplam a necessidade de alteração ou agendamento de horário, itinerário, transbordo, tipo de veículo e outros exigidos por normas específicas do local de entrega/coleta. Caso haja a necessidade, deverá ser informado no ato da cotação por obterem valores extras.</p>

<h2>1.4 Prazos e Condições Específicas</h2>
<p>Produtos químicos, eletrocalhas, perfis, ferragens, que possuem difícil acomodação, maiores de 6 metros em compartilhamento com outras mercadorias, acrescentar três dias no prazo de entrega.</p>

<h2>1.5 Dificuldades de Entrega/Coleta</h2>
<p>Entregas ou coletas em locais que apresentarem dificuldades como: supermercados, área rural, shoppings, etc., que exigirem recebimento por ordem de chegada, independente da quantidade, que tenha recebimento ou saída precária, que gere longas filas e tempo excessivo de descarga ou carregamento, exigência de separação de itens, exigência de tripulação/ajudantes superior à do veículo, devem ser informadas no ato da cotação para valores extras exigidos, caso contrário, será cobrada taxa de dificuldade de entrega e/ou taxa de difícil acesso, sem aviso prévio. (A aplicação de TDE e TDA não exclui a cobrança de estadia.)</p>

<h2>1.6 Prazo Máximo para Carga e Descarga</h2>
<p>Segundo a Lei Nº 11442 da ANTT, o prazo máximo para carga e descarga é de 5 horas, contados da chegada do veículo ao endereço de destino, na qual será devido a cobrança de um valor específico calculado por tonelada/hora ou fração. Valores atualizados conforme ANTT, verificar tabela.</p>

<h2>1.7 Obrigações do Emissor da Nota Fiscal</h2>
<p>É de obrigação total e completa do emissor da nota fiscal para transporte, inserir de forma clara e completa, todas as informações do destinatário para as operações de entrega e operações financeiras necessárias para a finalização do serviço. Não nos responsabilizamos por dados incorretos no documento e possíveis danos, taxas e custos que podem ocorrer referente a erros e/ou faltas de informações.</p>

<h2>1.8 Endereço do Destinatário</h2>
<p>Conforme a lei fiscal, não aceitamos local de entrega diferente do endereço do destinatário informado na nota fiscal.</p>

<h2>1.9 Documentação Fiscal Adequada</h2>
<p>É de obrigação do contratante munir a transportadora da documentação fiscal adequada atinente à carga, possibilitando o transporte regular das mercadorias.</p>

<h2>1.10 Compromisso com a Mercadoria</h2>
<p>A transportadora se comprometerá com o bem e os cuidados da mercadoria, nos termos do artigo 9º da Lei 11.442/2007, para todos os fins e efeitos legais, devendo informar o contratante pelo meio de comunicação cadastrado e concedido pelo mesmo em nota fiscal, a ocorrência de qualquer acidente ou apreensão de mercadorias envolvendo a carga.</p>

<h2>1.11 Notificação de Ocorrências</h2>
<p>A transportadora notificará o contratante de imediato sobre qualquer ocorrência de pane elétrica ou de qualquer espécie no veículo com o qual presta o serviço de transporte, que possa implicar atraso na entrega.</p>

<h2>1.12 Restrições de Transporte</h2>
<p>O Contratante se compromete a não utilizar o Frete para transportar: documentos de qualquer natureza, dinheiro ou cheque; produtos ilícitos; animais; armas de fogo ou munições; materiais perigosos, inflamáveis ou combustíveis; drogas e entorpecentes; explosivos; joias e quaisquer outros materiais cujo transporte seja proibido pela legislação ou precisem de licença de transporte específica.</p>

<h1>2. Taxas, Reentrega e Coleta - DigoTurismo</h1>

<h2>2.1 Taxa por Entrega Não Concretizada</h2>
<p>Sempre que, por responsabilidade do usuário, a entrega não puder ser concretizada na primeira tentativa, será cobrada uma taxa de 50% do valor do frete original.</p>

<h2>2.2 Taxa por Coleta não Concluída</h2>
<p>Caso a coleta não seja concluída no embarque e na data solicitada, será cobrada taxa de coleta referente ao valor de custo de distância e espera + danos que houver.</p>

<h2>2.3 Operação Fora do Horário Comercial</h2>
<p>Nosso serviço regular de transporte prevê a realização de coletas e entregas de segunda a sexta-feira em horário comercial. Serviços realizados fora dos dias e horários normais de operação, aos sábados, domingos, feriados, à noite, exigem pessoal em regime de hora extra, acarreta ociosidade nos veículos, custos administrativos aos terminais e possuem custos extras.</p>

<h2>2.4 Taxa por Restrição à Circulação</h2>
<p>Sempre que a coleta e entrega forem realizadas em municípios que possuam algum tipo de restrição à circulação de veículos de transporte de carga e/ou a própria atividade de carga e descarga, será cobrada taxa sob o percentual do frete.</p>

<h2>2.5 Taxa por Permanência em Áreas de Operação</h2>
<p>Quando houver uma permanência de carga em áreas de operação de transporte motivado por entraves fiscais que geram apreensões de mercadorias, nomeando o transportador como Fiel Depositário, será cobrada taxas a partir do 1º dia corrido a contar da data de envio de aviso aos responsáveis. Valor percentual da mercadoria ao acrescida de frete.</p>

<h2>2.6 Taxa em Situações de Beligerância</h2>
<p>Para regiões que se encontram em estado de beligerância que impeça a passagem ou finalização da entrega, haverá a taxa emex, para situações de emergência excepcional enquanto a situação não se normalizar.</p>

<h2>2.7 Taxa de Difícil Acesso</h2>
<p>Locais que tenham alguma dificuldade de acesso do veículo ao ponto de carregamento/descarga, seja por: Localidade com baixo volume de carga ou em áreas rurais, com trechos ao longo da estrada sem pavimentação ou pavimentação defeituosa demais que impeça a passagem do veículo, entre outras, será cobrada a taxa de difícil acesso sob um percentual do frete original.</p>

<h2>2.8 Custos Adicionais por Agendamento</h2>
<p>Entregas com agendamento geram custos adicionais operacionais e devem ser solicitadas anterior ao fechamento do frete para a verificação de custos extras, caso não seja feito, os mesmos serão cobrados sem aviso prévio.</p>

<h2>2.9 Taxa de Permanência de Carga</h2>
<p>Sempre que a mercadoria precisar permanecer armazenada por motivos que não dependem do transportador, como nos casos de agendamento, fiscalização, reentrega, restrição de trânsito, entre outros, será cobrada a taxa de permanência de carga, para isso será cobrado um valor sob as diárias de cada pallet armazenado.</p>

<h1>3. Materiais Frágeis e Embalagem - DigoTurismo</h1>

<h2>3.1 Danos a Materiais Frágeis</h2>
<p>Nossa seguradora não cobre danos causados no transporte de mercadorias consideradas frágeis tais como: Vidros, lâmpadas, acrílico, porcelanas, danos provenientes de mal acondicionamento da mercadoria por parte do fabricante e/ou com embalagens não adequadas para transporte rodoviário, com vícios próprios do bem transportado e afins. Na ocorrência desse fortuito, o cliente deve estar ciente dos riscos e não ressarcimento de sua mercadoria.</p>

<h2>3.2 Responsabilidade pela Embalagem</h2>
<p>A embalagem do produto é inteiramente de responsabilidade do cliente, cada produto merece uma embalagem específica, capaz de favorecer suas condições de transporte e apenas o cliente saberá os vícios próprios de sua mercadoria transportada. A transportadora não se responsabiliza por embalagens frágeis ou que não se adequam ao produto para o transporte rodoviário e o cliente deve estar ciente dos riscos e avarias que podem ocorrer.</p>

<h1>4. Financeiro - DigoTurismo</h1>

<h2>4.1 Negociação Financeira</h2>
<p>Questões financeiras como descontos, faturamentos, parcelamento, entre outros, devem ser negociadas anterior ao fechamento do frete. Após a confirmação da ordem de serviço e documentos fiscais gerados, não será possível atribuição ou mudança de valores.</p>

<h2>4.2 Frete FOB</h2>
<p>Todo frete FOB (Free on Board), na qual o destinatário da nota fiscal é o pagador, sempre será à vista, pagos no mesmo momento da solicitação dos serviços.</p>

<h2>4.3 Avaliação de Cadastro</h2>
<p>Todo faturamento necessita de uma avaliação e aprovação de cadastro que devem ser solicitada com os devidos documentos enviados antes ao fechamento do frete. (Verificar documentos necessários junto ao responsável por sua cotação e/ou departamento financeiro).</p>

<h2>4.4 Primeiro Frete</h2>
<p>Para todo primeiro frete não há faturamento, mesmo com análise cadastral, sendo assim, todo primeiro frete deve ser pago no mesmo momento da solicitação dos serviços.</p>

<h2>4.5 Forma de Pagamento</h2>
<p>Pagamentos para a DigoTurismo são sempre feitos via boleto bancário. Depósito somente com autorização excepcional solicitada e com respectiva autorização.</p>

<h2>4.6 Responsabilidade por Atraso no Pagamento</h2>
<p>Não nos responsabilizamos por juros e multa referente a atraso no pagamento, os mesmos são inteiramente de responsabilidade do pagador.</p>

