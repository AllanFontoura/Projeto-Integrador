<p>Ocorrências em nota fiscal, operacionais, financeiras, etc.; Podem atrasar a entrega e consequentemente aumentar o prazo/previsão final. E em tais fortuitos, é necessário que o cliente esteja ciente de tal e que esteja a par de todos os dados e obrigações conforme aqui citadas para evitar o evento.</p>

<h2>5.10 Devolução de Comprovante de Entrega</h2>
<p>Uma das vias do comprovante pertence ao arquivo da transportadora e é apresentado sempre que solicitado, assim, a devolução de canhotos das notas fiscais ou canhotos assinados para comprovar entrega, constitui serviço adicional não incluso no valor de sua cotação regular de frete e devem ser solicitadas anterior ao fechamento do frete para verificação de custos extras.</p>

