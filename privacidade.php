<h2>POLÍTICA DE PRIVACIDADE DE DADOS E USO DE COOKIES</h2>

<h3>INTRODUÇÃO</h3>
A Digoturismo, comprometida com a excelência no serviço prestado aos seus clientes, cumpre as legislações pertinentes ao setor de turismo, bem como as normas de proteção de dados e informações pessoais. Esta política estabelece os procedimentos para coleta, registro, armazenamento, uso, compartilhamento e eliminação de dados, em conformidade com a Lei Geral de Proteção de Dados (LGPD) - Lei nº 13.709/2018. <br> 

<b>DEFINIÇÕES</b><br>
Dados Pessoais: Referem-se a informações que identificam ou tornam identificável uma pessoa física.<br>
Usuário: Indivíduo que interage com os serviços oferecidos pela Digoturismo, seja por meio de compra de pacotes turísticos, reserva de hospedagem, solicitação de informações ou outras formas de contato.<br>
Cookies: Arquivos de informação armazenados no dispositivo do usuário, por meio do navegador de internet, que permitem registrar suas ações e preferências durante a navegação.<br>
Termo de Consentimento: Documento que autoriza o tratamento dos dados pessoais para finalidades específicas.<br>
Tratamento: Operações realizadas com dados pessoais, como coleta, armazenamento, utilização e compartilhamento.<br>

<h3>POLÍTICA DE PRIVACIDADE</h3>
<br>
<b>COLETA DE DADOS PESSOAIS</b><br>
Os dados pessoais são coletados durante a interação do usuário com os serviços da Digoturismo, incluindo a compra de pacotes turísticos, reserva de hospedagem e outras atividades relacionadas ao turismo. As informações coletadas podem incluir nome, e-mail, telefone, CPF, data de nascimento e endereço, conforme necessário para a prestação dos serviços.<br>
<br>
<b>COLETA DE DADOS PESSOAIS EM PLATAFORMAS DIGITAIS</b><br>
Ao utilizar o website, aplicativos ou outros canais digitais da Digoturismo, os usuários podem fornecer dados pessoais para a compra de serviços ou solicitação de informações adicionais.<br>
<br>
<b>PÁGINAS DE TERCEIROS PARCEIROS</b><br>
Ao acessar plataformas de parceiros da Digoturismo, os usuários estão sujeitos às políticas de privacidade desses terceiros, sendo recomendável que leiam atentamente tais políticas antes de fornecerem seus dados pessoais.<br>
<br>
<b>UTILIZAÇÃO DE DADOS PESSOAIS</b><br>
Os dados pessoais coletados são utilizados para a prestação dos serviços contratados pelo usuário, bem como para o cumprimento das obrigações legais da Digoturismo.<br>
<br>
<b>COMPARTILHAMENTO DE DADOS PESSOAIS</b><br>
A Digoturismo pode compartilhar dados pessoais com autoridades governamentais, prestadores de serviço e parceiros comerciais, quando necessário para a execução dos serviços contratados pelo usuário ou para o cumprimento de obrigações legais.<br>
<br>
<b>ARMAZENAMENTO E CONSERVAÇÃO DE DADOS PESSOAIS</b><br>
Os dados pessoais são armazenados pelo tempo necessário para cumprir as finalidades para as quais foram coletados, bem como para atender às obrigações legais aplicáveis.<br>
<br>
<b>DIREITOS DOS USUÁRIOS</b>
Os usuários têm o direito de acessar, corrigir, atualizar e solicitar a exclusão de seus dados pessoais, bem como de se opor ao seu tratamento, conforme previsto na legislação aplicável.<br>
<br>
<b>RECLAMAÇÕES E DÚVIDAS</b>
Em caso de dúvidas ou reclamações relacionadas ao tratamento dos dados pessoais pela Digoturismo, os usuários podem entrar em contato através do e-mail: privacidade@digoturismo.com.br.<br>
<br>
<b>POLÍTICA DE USO DE COOKIES</b>
A Digoturismo utiliza cookies em seu website e aplicativos para melhorar a experiência de navegação dos usuários. Os usuários podem configurar seus navegadores para aceitar ou recusar o uso de cookies, sendo que a recusa pode limitar algumas funcionalidades oferecidas.<br>
<br>
<b>ALTERAÇÕES NA POLÍTICA</b>
Esta Política de Privacidade e Uso de Cookies pode ser alterada pela Digoturismo a qualquer momento, sendo que as alterações serão comunicadas aos usuários de forma adequada.<br>
